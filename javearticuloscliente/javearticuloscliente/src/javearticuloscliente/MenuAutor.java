    public void cargarArticulos(){
        String []datos;
        List<Articulo> articulos = apiS.articuloUsuario(new BigDecimal(usu));
        removeAllRows(modelArticulo);
        boolean hay = false;
        for(Articulo a: articulos){
            hay = true;
            datos = new String[5];
            datos[0] = String.valueOf(a.getIdarticulo());
            datos[1] = String.valueOf(a.getTitulo());
            datos[2] = String.valueOf(a.getResumen());
            datos[3] = String.valueOf(a.getPalabrasclave());
            datos[4] = String.valueOf(a.getArchivo());
            modelArticulo.addRow(datos);
        }
        if(!hay){
            Utils.Util.mostrarMensaje("No hay articulos");
        }
    }

    public void removeAllRows(DefaultTableModel dtm){
        try{
            int rowCount = dtm.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                dtm.removeRow(i);
            }
        } catch (Exception e){
            
        }
    }
    
