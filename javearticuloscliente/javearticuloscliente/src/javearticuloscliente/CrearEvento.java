    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        // TODO add your handling code here:
        Evento ev = new Evento();
        ev.setTema(tema.getText());
        ev.setLugar(jTextField1.getText());
         try {
             ev.setFechainicio(Util.stringToXMLGC(fechaInicio.getText()));
             ev.setFechafin(Util.stringToXMLGC(fechaFin.getText()));
         } catch (ParseException ex) {
             Logger.getLogger(CrearEvento.class.getName()).log(Level.SEVERE, null, ex);
         } catch (DatatypeConfigurationException ex) {
             Logger.getLogger(CrearEvento.class.getName()).log(Level.SEVERE, null, ex);
         }
        api.crearEvento(ev);
        dispose();
    }//GEN-LAST:event_AceptarActionPerformed

