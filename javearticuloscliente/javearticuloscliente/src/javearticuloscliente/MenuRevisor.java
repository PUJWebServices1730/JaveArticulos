    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         if(jTable1.getSelectedRow() == -1){
            Utils.Util.mostrarMensaje("Seleccione un articulo");
            return;
        }
        
        String rutaProyecto = new File (".").getAbsolutePath ();
    	rutaProyecto = rutaProyecto.substring(0, rutaProyecto.length()-1) + "files/";
        String fileName = modelSubmision.getValueAt(jTable1.getSelectedRow(), 10).toString();
        try {
            File path = new File (rutaProyecto+fileName);
            Desktop.getDesktop().open(path);
       }catch (Exception ex) {
            Archivo.download(fileName);
       }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    public void removeAllRows(DefaultTableModel dtm){
        try{
            int rowCount = dtm.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                dtm.removeRow(i);
            }
        } catch (Exception e){
            
        }
    }
    
    public void cargarSubmisions(){
          
        String []datos;
        List<Revision> revisiones = apiS.revisionesRevisor(new BigDecimal(String.valueOf(usu)));
        removeAllRows(modelSubmision);
        boolean hay = false;
        for(Revision r: revisiones){
            hay = true;
            datos = new String[11];
            datos[0] = String.valueOf(r.getIdrevision());
            datos[1] = String.valueOf(r.getSubmisionIdsubmision().getIdsubmision());
            datos[2] = String.valueOf(r.getSubmisionIdsubmision().getUsuarioIdusuario().getIdusuario());
            datos[3] = String.valueOf(r.getSubmisionIdsubmision().getUsuarioIdusuario().getNombres() + " " + r.getSubmisionIdsubmision().getUsuarioIdusuario().getApellidos());
            datos[4] = String.valueOf(r.getSubmisionIdsubmision().getEventoIdevento().getIdevento());
            datos[5] = String.valueOf(r.getSubmisionIdsubmision().getEventoIdevento().getTema());
            datos[6] = String.valueOf(r.getSubmisionIdsubmision().getEventoIdevento().getLugar());
            datos[7] = String.valueOf(Utils.Util.XMLGCToString(r.getSubmisionIdsubmision().getEventoIdevento().getFechainicio()));
            datos[8] = String.valueOf(r.getSubmisionIdsubmision().getEstado());
            datos[9] = String.valueOf(r.getSubmisionIdsubmision().getCalificacion());
            datos[10] = String.valueOf(r.getSubmisionIdsubmision().getArticuloIdarticulo2().getArchivo());
            modelSubmision.addRow(datos);
        }
        if(!hay){
            Utils.Util.mostrarMensaje("No hay submisiones");
        }
        
        
    }
    
