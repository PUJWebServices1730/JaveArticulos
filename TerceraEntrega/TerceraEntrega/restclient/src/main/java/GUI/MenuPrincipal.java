    public void botonesRoles(){
        if(rol.equals("editor")){
            this.crearEvento.setEnabled(true);
            this.crearArticulo.setEnabled(false);
            this.enviarArticulo.setEnabled(false);
            this.calificarArticulo.setEnabled(false);
        }
        if(rol.equals("autor")){
            this.crearEvento.setEnabled(false);
            this.crearArticulo.setEnabled(true);
            this.enviarArticulo.setEnabled(true);
            this.calificarArticulo.setEnabled(false);
            this.asignarRevisor.setEnabled(false);
        }
        
        if(rol.equals("revisor")){
            this.crearEvento.setEnabled(false);
            this.crearArticulo.setEnabled(false);
            this.enviarArticulo.setEnabled(false);
            this.calificarArticulo.setEnabled(true);
            this.asignarRevisor.setEnabled(false);
        }
    }
