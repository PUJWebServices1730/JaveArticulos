    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        Articulo arti = new Articulo();
        arti.setTitulo(titulo.getText());
        arti.setResumen(resumen.getText());
        arti.setPalabrasclave(palabras.getText());
        
        if(cliente.crearArticulo(token, arti)!=null){
            JOptionPane.showMessageDialog(null, "Articulo creado con exito");
            this.setVisible(false);
        }
    }//GEN-LAST:event_crearActionPerformed

